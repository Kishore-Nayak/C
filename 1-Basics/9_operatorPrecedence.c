/*
Operator Precedence in C:

1. Postfix:           expr++   expr--
2. Prefix:            ++expr   --expr   +expr   -expr   ~   !
3. Multiplicative:    *   /   %
4. Additive:          +   -
5. Shift:             <<   >>
6. Relational:        <   <=   >   >=
7. Equality:          ==   !=
8. Bitwise AND:       &
9. Bitwise XOR:       ^
10. Bitwise OR:       |
11. Logical AND:      &&
12. Logical OR:       ||
13. Conditional:       ?:
14. Assignment:        =   +=   -=   *=   /=   %=   <<=   >>=   &=   ^=   |=
15. Comma:            ,

Note: Operators of the same precedence level are evaluated left to right (or right to left for assignment operators).
*/
